package com.javaOpearator;

public class Example2 {
//rational operator
	public static void main(String[] args) {
		int a = 500;
		int b = 500;
		System.out.println(">:" +(a > b)); // a=false
		System.out.println("<:" +(a < b)); // a=false
		System.out.println(">=:" +(a >= b)); //a=true
		System.out.println("<=:" +(a <= b));//a=true
		System.out.println("==:" +(a == b));//a=true
		System.out.println("!=:" +(a != b));//a=false
		
		
		
		
		

	}

}
